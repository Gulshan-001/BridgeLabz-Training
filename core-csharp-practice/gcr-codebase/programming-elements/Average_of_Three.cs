using System;

public class Average_of_Three
{
    public static void Main(string[] args)
    {
        int num1 = 10;
        int num2 = 20;
        int num3 = 30;
        double average = (num1 + num2 + num3) / 3.0;
        Console.WriteLine("The average of " + num1 + ", " + num2 + ", and " + num3 + " is: " + average);
    }
}
